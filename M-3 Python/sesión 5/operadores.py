# operadores logicos
# and, or, not

a = True
b = False

print(a and b) # imprime False, se tiene que cumplir las dos condiciones en True
print(a or b) # imprime True, se tiene que cumplir una de las dos concidiones en True
print(not a) # Imprime False, cambia el valor de la variable
print(not (a and b)) # imprime True, ya que se cambia el valor de la variable a 
print(not (a or b)) # imprime False 

# Operadores de comparacion
# ==, !=, <, >, <=, >=

c = 5
d = 10

print(c == d) # imprime False, pregunta si c es igual a d
print( c != d) # imprime True, pregunta si c es diferente a d
print(c < d) # imprime True, pregunta si c es menor que d
print(c > d) # imprime False, pregunta si c es mayor a d
print(c <= d) # imprime True, pregunta si c en menor o igual a d
print(c >= d) # imprime False, pregunta si c en mayor o igual a d

